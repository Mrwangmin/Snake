public class Food {
    int x;
    int y;
    Food(int x, int y){
        this.x = x;
        this.y = y;
    }
    Food(){}
    void setX(int x){
        this.x = x;
    }
    void setY(int y){
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
